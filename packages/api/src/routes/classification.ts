/** POST /v1/ledgers/:ledgerId/classification/aliases — Add merchant alias */
classificationRoutes.post("/aliases", async (c) => {
  const engine = c.get("engine");
  const body = await c.req.json();

  if (!body.canonicalName || !body.alias) {
    return errorResponse(
      c,
      createError(ErrorCode.VALIDATION_ERROR, "canonicalName and alias are required", [
        {
          field: "canonicalName, alias",
          suggestion: "Provide the canonical merchant name and the alias to map to it.",
        },
      ]),
    );
  }

  const result = await engine.addMerchantAlias(body.canonicalName, body.alias);
  if (!result.ok) return errorResponse(c, result.error);

  return created(c, result.value);
});
