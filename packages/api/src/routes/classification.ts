/** PUT /v1/ledgers/:ledgerId/classification/rules/:ruleId — Update classification rule */
classificationRoutes.put("/rules/:ruleId", async (c) => {
  const engine = c.get("engine");
  const ruleId = c.req.param("ruleId")!;
  const body = await c.req.json();

  const result = await engine.updateClassificationRule(ruleId, {
    priority: body.priority,
    pattern: body.pattern,
    targetAccountId: body.targetAccountId,
    isPersonal: body.isPersonal,
    confidence: body.confidence,
  });
  if (!result.ok) return errorResponse(c, result.error);

  return success(c, result.value);
});

/** DELETE /v1/ledgers/:ledgerId/classification/rules/:ruleId — Delete classification rule */
classificationRoutes.delete("/rules/:ruleId", async (c) => {
  const engine = c.get("engine");
  const ruleId = c.req.param("ruleId")!;

  const result = await engine.deleteClassificationRule(ruleId);
  if (!result.ok) return errorResponse(c, result.error);

  return c.json({ data: { deleted: true } });
});

