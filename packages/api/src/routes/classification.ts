// ---------------------------------------------------------------------------
// Classification routes — /v1/ledgers/:ledgerId/classification
//
// CRUD for classification rules, classify preview, manual bank transaction
// classification.
// ---------------------------------------------------------------------------

import { Hono } from "hono";
import type { Env } from "../lib/context.js";
import { apiKeyAuth } from "../middleware/auth.js";
import { errorResponse, created, success } from "../lib/responses.js";
import { createError, ErrorCode } from "@ledge/core";
import type { RuleType, RuleField } from "@ledge/core";

export const classificationRoutes = new Hono<Env>();

classificationRoutes.use("/*", apiKeyAuth);

// ---------------------------------------------------------------------------
// Rules CRUD
// ---------------------------------------------------------------------------

/** GET /v1/ledgers/:ledgerId/classification/rules — List classification rules */
classificationRoutes.get("/rules", async (c) => {
  const engine = c.get("engine");
  const ledgerId = c.req.param("ledgerId")!;

  const ruleType = c.req.query("ruleType") as RuleType | undefined;
  const field = c.req.query("field") as RuleField | undefined;
  const autoGeneratedStr = c.req.query("autoGenerated");
  const autoGenerated = autoGeneratedStr !== undefined
    ? autoGeneratedStr === "true"
    : undefined;
  const limitStr = c.req.query("limit");
  const limit = limitStr ? parseInt(limitStr, 10) : undefined;
  const offsetStr = c.req.query("offset");
  const offset = offsetStr ? parseInt(offsetStr, 10) : undefined;

  const result = await engine.listClassificationRules(ledgerId, {
    ruleType,
    field,
    autoGenerated,
    limit,
    offset,
  });
  if (!result.ok) return errorResponse(c, result.error);

  return success(c, result.value);
});

/** POST /v1/ledgers/:ledgerId/classification/rules — Create classification rule */
classificationRoutes.post("/rules", async (c) => {
  const engine = c.get("engine");
  const ledgerId = c.req.param("ledgerId")!;
  const body = await c.req.json();

  if (!body.ruleType || !body.field || !body.pattern || !body.targetAccountId) {
    return errorResponse(
      c,
      createError(ErrorCode.VALIDATION_ERROR, "Missing required fields", [
        {
          field: "ruleType, field, pattern, targetAccountId",
          suggestion:
            "Provide ruleType (exact|contains|regex|amount_range), field (description|merchant_name|category), pattern, and targetAccountId.",
        },
      ]),
    );
  }

  const result = await engine.createClassificationRule({
    ledgerId,
    ruleType: body.ruleType,
    field: body.field,
    pattern: body.pattern,
    targetAccountId: body.targetAccountId,
    priority: body.priority,
    isPersonal: body.isPersonal,
    confidence: body.confidence,
  });
  if (!result.ok) return errorResponse(c, result.error);

  return created(c, result.value);
});

