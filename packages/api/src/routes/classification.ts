// ---------------------------------------------------------------------------

/** POST /v1/ledgers/:ledgerId/classification/bank-transactions/:bankTransactionId — Manually classify */
classificationRoutes.post("/bank-transactions/:bankTransactionId", async (c) => {
  const engine = c.get("engine");
  const bankTransactionId = c.req.param("bankTransactionId")!;
  const body = await c.req.json();

  if (!body.accountId) {
    return errorResponse(
      c,
      createError(ErrorCode.VALIDATION_ERROR, "accountId is required", [
        {
          field: "accountId",
          suggestion: "Provide the ledger account ID to classify this transaction to.",
        },
      ]),
    );
  }

  const result = await engine.classifyBankTransaction(
    bankTransactionId,
    body.accountId,
    body.isPersonal ?? false,
  );
  if (!result.ok) return errorResponse(c, result.error);

  return success(c, result.value);
});

