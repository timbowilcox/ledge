/** POST /v1/ledgers/:ledgerId/classification/classify — Preview classification */
classificationRoutes.post("/classify", async (c) => {
  const engine = c.get("engine");
  const ledgerId = c.req.param("ledgerId")!;
  const body = await c.req.json();

  if (!body.description) {
    return errorResponse(
      c,
      createError(ErrorCode.VALIDATION_ERROR, "description is required", [
        {
          field: "description",
          suggestion: "Provide a transaction description to classify.",
        },
      ]),
    );
  }

  const result = await engine.classifyTransaction(ledgerId, {
    description: body.description,
    category: body.category,
    amount: body.amount,
  });
  if (!result.ok) return errorResponse(c, result.error);

  return success(c, result.value);
});

